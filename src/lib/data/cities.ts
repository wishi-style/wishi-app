/**
 * Filter cities against a typed query. Case-insensitive substring match on
 * city name, region (US state), OR country. Empty / very short queries
 * return [] (no suggestions surfaced until the user has typed something
 * meaningful).
 *
 * Matches earlier in the city name are ranked higher — typing "new" surfaces
 * "New York" before "New Haven" before "Newcastle" before "Stockton" (which
 * contains "ock" but not as a prefix).
 */
export function filterCities(query: string, limit = 8): City[] {
  const q = query.trim().toLowerCase();
  if (q.length < 2) return [];

  type Scored = { city: City; score: number };
  const matches: Scored[] = [];

  for (const c of CITIES) {
    const nameLower = c.name.toLowerCase();
    const regionLower = c.region?.toLowerCase() ?? "";
    const countryLower = c.country.toLowerCase();

    let score = -1;
    if (nameLower.startsWith(q)) score = 0; // best
    else if (nameLower.includes(q)) score = 1;
    else if (regionLower === q) score = 2; // exact state match (e.g. "ca")
    else if (countryLower.includes(q)) score = 3;
    else if (regionLower.includes(q)) score = 4;

    if (score >= 0) matches.push({ city: c, score });
  }

  matches.sort((a, b) => a.score - b.score);
  return matches.slice(0, limit).map((m) => m.city);
}

export function formatCity(city: City): string {
  return city.region
    ? `${city.name}, ${city.region}, ${city.country}`
    : `${city.name}, ${city.country}`;
}
